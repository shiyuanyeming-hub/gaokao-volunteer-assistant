// Map query intent to relevant heuristics
export function getHeuristicsForIntent(intent: string): string[] {
  const map: Record<string, string[]> = {
    major: ["medianPrinciple", "irreplaceability", "fortune500"],
    school: ["fortune500", "cityPriority", "medianPrinciple"],
    score: ["soulQuestions", "familyBackground"],
    postgraduate: ["tenYearTest", "irreplaceability"],
    career: ["familyBackground", "cityPriority", "tenYearTest"],
    city: ["cityPriority", "medianPrinciple"],
    volunteer: ["soulQuestions", "admissionChecks", "officialSources", "familyBackground"],
    family: ["familyBudgetReality", "familyBackground", "cityPriority"],
    source: ["officialSources", "admissionChecks"],
    crisis: ["crisisSafety"],
    default: ["soulQuestions", "medianPrinciple", "officialSources"],
  };

  const keys = map[intent] || map["default"];
  return keys.map((k) => HEURISTICS[k as keyof typeof HEURISTICS]);
}
