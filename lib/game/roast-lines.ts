const GENERIC: Reaction[] = [
  { line: "张老师看完这选择：不算离谱，但也别急着感动自己。", mood: "皱眉" },
  { line: "这选项有点想法，也有点危险。算成本的时候别只算分。", mood: "皱眉" },
  { line: "选都选了——那我跟你说说这背后的账怎么算。", mood: "皱眉" },
];

function pickGeneric(optionId: string): Reaction {
  const idx =
    Math.abs(optionId.split("").reduce((sum, ch) => sum + ch.charCodeAt(0), 0)) %
    GENERIC.length;
  return GENERIC[idx];
}

export function pickReaction(option: GameOption, state: GameState): Reaction {
  const entry = REACTIONS[option.id];
  if (!entry) return pickGeneric(option.id);
  return typeof entry === "function" ? entry(state) : entry;
}
